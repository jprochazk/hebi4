#[inline(always)]
unsafe fn istrue(args: Istrue, jt: Jt, sp: Sp, lp: Lp, ip: Ip, ctx: Ctx) -> Control {
    // istrue v
    // jmp offset

    let v = *sp.at(args.v);
    if v.coerce_bool() {
        // skip `jmp`
        let ip = ip.offset(2);
        dispatch_current(jt, sp, lp, ip, ctx)
    } else {
        // execute `jmp`
        let ip = ip.offset(1);
        dispatch_current(jt, sp, lp, ip, ctx)
    }
}

