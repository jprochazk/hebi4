macro_rules! try_div_eval {
    ($dst:ident, $lhs:ident, $rhs:ident, $ctx:ident, $ip:ident) => {
        use ValueRaw::*;
        match ($lhs, $rhs) {
            (Int(lhs), Int(rhs)) => {
                if rhs == 0 {
                    vm_exit!($ctx, $ip, DivisionByZero);
                }

                *$dst = Int(lhs / rhs);
            }

            // float div by zero = inf
            (Float(lhs), Float(rhs)) => {
                *$dst = Float(lhs / rhs);
            }
            (Float(lhs), Int(rhs)) => {
                *$dst = Float(lhs / (rhs as f64));
            }
            (Int(lhs), Float(rhs)) => {
                *$dst = Float((lhs as f64) / rhs);
            }
            _ => {
                vm_exit!($ctx, $ip, ArithTypeError);
            }
        }
    };
}

